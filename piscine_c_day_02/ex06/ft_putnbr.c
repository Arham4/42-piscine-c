int		ft_putchar(char c);

void	ft_nbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb < 10)
	{
		ft_putchar(nb + '0');
	}
	else
	{
		ft_nbr(nb / 10);
		ft_nbr(nb % 10);
	}
}
