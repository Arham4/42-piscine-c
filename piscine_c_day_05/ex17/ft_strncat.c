char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int length;

	i = 0;
	length = 0;
	while (dest[length] != '\0')
	{
		length++;
	}
	while (i < nb && src[i] != '\0')
	{
		dest[length + i] = src[i];
		i++;
	}
	dest[length + i] = '\0';
	return (dest);
}
