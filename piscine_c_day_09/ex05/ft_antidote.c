int		ft_antidote(int i, int j, int k)
{
	if ((k >= i && k <= j) || (k >= j && k <= i))
	{
		return (k);
	}
	else if ((j >= i && j <= k) || (j >= k && j <= i))
	{
		return (j);
	}
	else if ((i >= k && i <= j) || (i >= j && i <= k))
	{
		return (i);
	}
	return (0);
}
