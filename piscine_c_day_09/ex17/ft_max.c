int		ft_max(int *tab, int length)
{
	int i;
	int max_i;

	i = 1;
	max_i = 0;
	while (i < length)
	{
		if (tab[i] > tab[max_i])
		{
			max_i = i;
		}
		i++;
	}
	return (tab[max_i]);
}
